// Build the lookup map
function buildMap(): Record<string, number> {
  const map: Record<string, number> = {}
  for (const word of N5) map[word] = 5
  for (const word of N4) map[word] = 4
  for (const word of N3) map[word] = 3
  for (const word of N2) map[word] = 2
  for (const word of N1) map[word] = 1
  return map
}

/** Maps base form → JLPT level (5=N5, 1=N1). Returns undefined for unknown words. */
export const JLPT_VOCABULARY: Record<string, number> = buildMap()

/** Look up the JLPT level for a word. Returns undefined if not in the dictionary. */
export function getJLPTLevel(word: string): number | undefined {
  return JLPT_VOCABULARY[word]
}
